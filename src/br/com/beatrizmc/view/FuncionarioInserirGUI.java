    private void btSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSalvarActionPerformed
        Funcionario u = new Funcionario();

        if (!(txCodigo.getText().equals("")) || (txCodigo.getText().equals(null))) {
            u.setCodigo(Integer.parseInt(txCodigo.getText()));
        }

        u.setNome(txNome.getText());
        u.setCpf(txCpf.getText());
        u.setCep(txCep.getText());
        u.setRg(txRg.getText());
        u.setTelefone(txTelefone.getText());
        u.setBairro(txBairro.getText());
        u.setRua(txRua.getText());
        u.setNumero(Integer.parseInt(txNumero.getText()));
        u.setCidade(txCidade.getText());
        u.setComplemento(txComplemento.getText());
        u.setTelefone1(txTelefone1.getText());
        u.setPis(txPis.getText());
        u.setCelular(txCelular.getText());
        u.setEstadoCivil(txEstadoCivil.getText());
        u.setSexo(txSexo.getText());
        u.setSal(Double.parseDouble(txSal1.getText()));
        u.setNascionalidade(txNascionalidade.getText());
        u.setEmail(txEmail.getText());
        u.setLogin(txUsuario.getText());
        u.setSenha(txSenha.getText());
        u.setHora(txHora.getText());
        u.setUf(txUf.getText());

        try {
            String data = txDataNasc.getText();
            u.setDtNascimento(new SimpleDateFormat("dd/MM/yyyy").parse(data));
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Erro ao converter a data: " + ex);
        }


        FuncionarioController uc = new FuncionarioController();


        if (u.getCodigo() == 0) {
            int id = uc.salvar(u);
            if (id > 0) {
                modelo.addRow(new Object[]{id, u.getNome(), u.getCpf(), u.getLogin(),u.getSenha()});
                JOptionPane.showMessageDialog(null, "FuncionÃ¡rio cadastrado com sucesso.");
            }
        } else {
            int id = uc.salvar(u);
            if (id > 0) {
                modelo.removeRow(linhaSelecionada);
                modelo.addRow(new Object[]{id, u.getNome(), u.getCpf(), u.getLogin()});
                JOptionPane.showMessageDialog(null, "FuncionÃ¡rio atualizado com sucesso.");
            }
        }
        dispose();

    }//GEN-LAST:event_btSalvarActionPerformed

